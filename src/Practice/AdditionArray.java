package Practice;

import java.util.Scanner;

public class AdditionArray{
    public static void main(String[] args) {
        int[][] arr = new int[3][3];
        for(int i = 0;i<arr.length ; i++){
            for(int j = 0;j<arr.length ; j++) {
                System.out.println(arr[i][j]);
            }
        }

    }
}
